#!/usr/bin/env node
// Regenerate cinatra-dev-extensions.lock.json — the committed, SHA-pinned
// clone-back lock for the dev-shape extension universe (cinatra#141).
//
// CI materializes every `cinatraDevExtensions` companion repo before
// `pnpm install --frozen-lockfile`; without pins, those checkouts float on
// the companion repos' branch tips, so a companion merge can break host
// `main` with zero host commits (the #138 incident class). The pin set is
// PARTITIONED across two locks, with no overlap:
//   - cinatra-required-extensions.lock.json — the prod bootable set; the
//     image build's acquisition source and the SINGLE authority for those
//     packages (regenerated by update-required-extension-lock.mjs, NEVER by
//     this script);
//   - cinatra-dev-extensions.lock.json (this script's output) — every OTHER
//     cinatraDevExtensions entry: {packageName, repo, resolvedSha} only. A
//     git checkout at the commit sha needs no tarball treeSha256 — the sha
//     IS the content pin; tarball integrity stays prod-acquisition-only.
//
// This is a DEV/BUMP-time tool: `git ls-remote` is fine here. CI never runs
// it — pinned sync consumes only the committed locks.
//
// Bump recipe (one deliberate PR — see docs/extension-clone-pinning.md):
//   node scripts/extensions/update-dev-extension-lock.mjs   # or --select a,b
//   node scripts/ci/sync-dev-extensions.mjs --pinned        # re-pin the tree
//   pnpm install                                            # pnpm-lock.yaml moves WITH the pins
//   node scripts/extensions/generate-extension-manifest.mjs # maps match the pinned tree
//
// Usage:
//   node scripts/extensions/update-dev-extension-lock.mjs              # regenerate all
//   node scripts/extensions/update-dev-extension-lock.mjs --select a,b # only listed packages
//                                                                      # (others keep current pins)

import { execFileSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import {
  DEV_EXTENSIONS_LOCK_FILENAME,
  REQUIRED_EXTENSIONS_LOCK_FILENAME,
} from "../../packages/cli/src/cinatra-dev-extensions.mjs";

const COMMIT_SHA_RE = /^[0-9a-f]{40}$/;

const shortName = (n) => String(n).replace(/^@[^/]+\//, "");

function repoSlugFromUrl(url, pkgName) {
  const m = String(url).match(/^https:\/\/github\.com\/([A-Za-z0-9._-]+\/[A-Za-z0-9._-]+?)(?:\.git)?$/);
  if (!m) {
    throw new Error(
      `${pkgName}: unsupported repo URL (expected https://github.com/<owner>/<name>[.git]): ${url}`,
    );
  }
  return m[1];
}

/**
 * Pure lock computation (CLI shell below; tests exercise this directly).
 *
 * - Targets are EXACTLY the `cinatraDevExtensions` entries NOT named in the
 *   required lock (disjoint partition — never a duplicate authority).
 * - `--select` is fail-closed: a token matching no target is an error (a
 *   typo'd select must not silently keep every existing pin and exit green).
 * - Unselected targets keep their existing pin; a target with NO existing pin
 *   must be selected (or run without --select) — never silently unpinned.
 */
export function computeDevLock({ config, requiredLockNames, existingPackages = [], select = [], resolveHead }) {
  const targets = Object.entries(config)
    .filter(([pkgName]) => !requiredLockNames.has(pkgName))
    .map(([pkgName, rawSpec]) => {
      const spec = rawSpec && typeof rawSpec === "object" ? rawSpec : { url: String(rawSpec) };
      return { pkgName, url: spec.url, branch: spec.branch || "main" };
    });
  if (targets.length === 0) {
    return { packages: [], resolvedCount: 0, keptCount: 0 };
  }

  if (select.length > 0) {
    const matched = new Set();
    for (const token of select) {
      const hit = targets.find((t) => t.pkgName === token || shortName(t.pkgName) === token);
      if (!hit) {
        throw new Error(
          `--select "${token}" matches no non-required cinatraDevExtensions entry — refusing a vacuous ` +
            `regeneration (required-lock packages are bumped via update-required-extension-lock.mjs).`,
        );
      }
      matched.add(hit.pkgName);
    }
    select = matched;
  }

  const existingByName = new Map(existingPackages.map((p) => [p.packageName, p]));
  const packages = [];
  let resolvedCount = 0;
  let keptCount = 0;
  const selecting = select instanceof Set && select.size > 0;
  for (const t of targets.sort((a, b) => a.pkgName.localeCompare(b.pkgName))) {
    const repo = repoSlugFromUrl(t.url, t.pkgName);
    if (selecting && !select.has(t.pkgName)) {
      const existing = existingByName.get(t.pkgName);
      if (!existing) {
        throw new Error(
          `${t.pkgName}: not selected and has NO existing pin — include it in --select or run without --select.`,
        );
      }
      // A kept pin must still point at the repo the config names. Silently
      // rewriting `repo` around an old sha would mask a retarget (the pinned
      // loader's slug guard would then pass a sha that was never resolved
      // against the new repo) — a retargeted package must be re-pinned.
      if (typeof existing.repo !== "string" || existing.repo.toLowerCase() !== repo.toLowerCase()) {
        throw new Error(
          `${t.pkgName}: the config now targets "${repo}" but the kept pin was resolved against ` +
            `"${existing.repo}" — a retargeted repo must be re-pinned (add it to --select).`,
        );
      }
      // A kept pin must also be shape-valid — never copy a corrupt sha forward.
      if (typeof existing.resolvedSha !== "string" || !COMMIT_SHA_RE.test(existing.resolvedSha)) {
        throw new Error(
          `${t.pkgName}: the existing pin "${existing.resolvedSha}" is not a 40-hex commit sha — ` +
            `re-resolve it (add it to --select or run without --select).`,
        );
      }
      packages.push({ packageName: t.pkgName, repo, resolvedSha: existing.resolvedSha });
      keptCount++;
      continue;
    }
    const sha = resolveHead({ pkgName: t.pkgName, url: t.url, repo, branch: t.branch });
    if (typeof sha !== "string" || !COMMIT_SHA_RE.test(sha)) {
      throw new Error(`${t.pkgName}: resolved head "${sha}" is not a 40-hex commit sha.`);
    }
    packages.push({ packageName: t.pkgName, repo, resolvedSha: sha });
    resolvedCount++;
  }
  return { packages, resolvedCount, keptCount };
}

// ---------------------------------------------------------------------------
// CLI shell
// ---------------------------------------------------------------------------

function main() {
  const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "..");
  const LOCK_PATH = path.join(REPO_ROOT, DEV_EXTENSIONS_LOCK_FILENAME);

  const fail = (msg) => {
    console.error(`[update-dev-extension-lock] ERROR: ${msg}`);
    process.exit(1);
  };

  let rawSelect = null;
  const argv = process.argv.slice(2);
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--select") rawSelect = argv[i + 1] ?? "";
    else if (argv[i].startsWith("--select=")) rawSelect = argv[i].slice("--select=".length);
  }
  const select = rawSelect !== null ? rawSelect.split(",").map((s) => s.trim()).filter(Boolean) : [];
  if (rawSelect !== null && select.length === 0) fail("--select was given but no package names were provided");

  const rootPkg = JSON.parse(readFileSync(path.join(REPO_ROOT, "package.json"), "utf8"));
  const config = rootPkg?.cinatraDevExtensions ?? rootPkg?.cinatra?.devExtensions;
  if (!config || typeof config !== "object" || Object.keys(config).length === 0) {
    fail("cinatraDevExtensions is empty/absent in the root package.json");
  }

  const requiredLockPath = path.join(REPO_ROOT, REQUIRED_EXTENSIONS_LOCK_FILENAME);
  if (!existsSync(requiredLockPath)) fail(`${REQUIRED_EXTENSIONS_LOCK_FILENAME} not found — it defines the partition.`);
  const requiredLockNames = new Set(
    (JSON.parse(readFileSync(requiredLockPath, "utf8")).packages ?? []).map((p) => p.packageName),
  );

  let existingPackages = [];
  if (existsSync(LOCK_PATH)) {
    try {
      existingPackages = JSON.parse(readFileSync(LOCK_PATH, "utf8")).packages ?? [];
    } catch {
      // unreadable existing lock: every target must be (re)resolved
    }
  }

  const resolveHead = ({ pkgName, url, branch }) => {
    let out;
    try {
      out = execFileSync("git", ["ls-remote", url, `refs/heads/${branch}`], {
        encoding: "utf8",
        timeout: 60_000,
      });
    } catch (err) {
      fail(`${pkgName}: git ls-remote ${url} failed: ${err.message}`);
    }
    const sha = out.split(/\s+/)[0];
    if (!COMMIT_SHA_RE.test(sha ?? "")) fail(`${pkgName}: git ls-remote ${url} returned no ${branch} head`);
    console.log(`  ${pkgName}: ${branch} @ ${sha}`);
    return sha;
  };

  let result;
  try {
    result = computeDevLock({ config, requiredLockNames, existingPackages, select, resolveHead });
  } catch (err) {
    fail(err.message);
  }

  const doc = {
    note:
      "Committed clone-back pins for the NON-required cinatraDevExtensions universe (cinatra#141). " +
      "CI checks each companion repo out detached at resolvedSha (scripts/ci/sync-dev-extensions.mjs --pinned); " +
      `the prod bootable set is pinned separately in ${REQUIRED_EXTENSIONS_LOCK_FILENAME} (never duplicated here). ` +
      "Regenerate with scripts/extensions/update-dev-extension-lock.mjs and move pnpm-lock.yaml + the generated " +
      "maps in the SAME PR — see docs/extension-clone-pinning.md.",
    schemaVersion: 1,
    packages: result.packages,
  };
  writeFileSync(LOCK_PATH, `${JSON.stringify(doc, null, 2)}\n`);
  console.log(
    `[update-dev-extension-lock] wrote ${result.packages.length} pins to ${DEV_EXTENSIONS_LOCK_FILENAME} ` +
      `(${result.resolvedCount} re-resolved, ${result.keptCount} kept).`,
  );
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main();
}

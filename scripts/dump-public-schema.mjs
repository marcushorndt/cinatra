#!/usr/bin/env node
/**
 * Dump the `public` schema DDL (Better Auth + LangGraph + misc app tables)
 * from a working local DB into a CI seed file. The RBAC browser e2e CI job
 * applies this seed so a fresh Postgres has the tables the dev server's
 * instrumentation hook + the auth fixtures need — without the better-auth
 * CLI (which refuses to load the Next.js auth config because of
 * `import "server-only"` in `better-auth/next-js`).
 *
 *   node --env-file=.env.local scripts/dump-public-schema.mjs
 *
 * Output: tests/e2e/rbac/fixtures/public-schema.sql
 *
 * Order: extensions → CREATE TABLE (columns only) → ADD CONSTRAINT (PK,
 * unique, FK, check) → CREATE INDEX. All idempotent (IF NOT EXISTS / guarded
 * DO blocks) so re-applying is safe.
 */
import { Client } from "pg";
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";

const OUT = "tests/e2e/rbac/fixtures/public-schema.sql";
const DATABASE_URL = process.env.SUPABASE_DB_URL;
if (!DATABASE_URL) {
  console.error("SUPABASE_DB_URL required");
  process.exit(1);
}

const c = new Client({ connectionString: DATABASE_URL });
await c.connect();

// Defense-in-depth escaping. The dumper introspects pg_catalog and writes SQL
// that scripts/apply-public-schema.mjs executes verbatim in CI; an attacker who
// controls the LOCAL DB at dump-time (or a careless reviewer of a poisoned
// snapshot) could otherwise inject SQL via crafted identifier/constraint names.
// `pg_get_constraintdef` and `pg_indexes.indexdef` are pg-generated and self-
// quoted, so they stay verbatim; everything else routes through these helpers.
function quoteIdent(name) {
  if (typeof name !== "string") throw new Error(`quoteIdent: expected string, got ${typeof name}`);
  return `"${name.replace(/"/g, '""')}"`;
}
function quoteLiteral(value) {
  if (typeof value !== "string") throw new Error(`quoteLiteral: expected string, got ${typeof value}`);
  return `'${value.replace(/'/g, "''")}'`;
}

const out = [];
out.push("-- GENERATED by scripts/dump-public-schema.mjs — do not edit by hand.");
out.push("-- Faithful snapshot of the public schema the RBAC e2e runs against.");
out.push("-- Regenerate from a working local DB with:");
out.push("--   node --env-file=.env.local scripts/dump-public-schema.mjs");
out.push("");
// Pin the search_path so unqualified references in introspected defaults /
// FK definitions (e.g. nextval('<seq>'::regclass), REFERENCES assistant(...))
// resolve regardless of the connecting role's default search_path.
out.push("SET search_path = public, pg_catalog;");
out.push("");

// 1. Extensions. None of the tables/indexes below actually depend on these
// (the GIN indexes use the built-in `jsonb_path_ops`), so wrap each in a
// soft DO block: a CI postgres missing a contrib extension must not abort
// the whole seed.
const ext = await c.query(
  `SELECT extname FROM pg_extension WHERE extname NOT IN ('plpgsql') ORDER BY extname`,
);
for (const r of ext.rows) {
  out.push(
    `DO $$ BEGIN\n` +
      `  CREATE EXTENSION IF NOT EXISTS ${quoteIdent(r.extname)};\n` +
      `EXCEPTION WHEN OTHERS THEN\n` +
      `  RAISE NOTICE ${quoteLiteral(`skipping extension ${r.extname}: %`)}, SQLERRM;\nEND $$;`,
  );
}
out.push("");

// 1b. Sequences — emitted before tables so column defaults referencing
// nextval('<seq>'::regclass) (e.g. bigserial columns) resolve. A fresh CI
// seed starts these at 1/1; OWNED BY linkage is cosmetic here so we skip it.
const seqRes = await c.query(
  `SELECT sequencename FROM pg_sequences WHERE schemaname = 'public' ORDER BY sequencename`,
);
for (const r of seqRes.rows) {
  out.push(`CREATE SEQUENCE IF NOT EXISTS public.${quoteIdent(r.sequencename)};`);
}
if (seqRes.rows.length) out.push("");

// 2. Base tables in public (exclude extension-owned views like pg_stat_statements).
const tablesRes = await c.query(`
  SELECT c.relname AS table
    FROM pg_class c
    JOIN pg_namespace n ON n.oid = c.relnamespace
   WHERE n.nspname = 'public' AND c.relkind = 'r'
     AND c.relname NOT LIKE 'pg_stat_statements%'
   ORDER BY c.relname
`);
const tables = tablesRes.rows.map((r) => r.table);

// 2a. CREATE TABLE with columns only (no constraints yet → no FK ordering issues).
for (const t of tables) {
  const cols = await c.query(
    `SELECT column_name, data_type, udt_name, is_nullable, column_default,
            character_maximum_length, numeric_precision, numeric_scale
       FROM information_schema.columns
      WHERE table_schema = 'public' AND table_name = $1
      ORDER BY ordinal_position`,
    [t],
  );
  const lines = cols.rows.map((col) => {
    // For pg builtin types (text, integer, boolean, varchar, numeric, …) the
    // type token is a keyword, not an identifier, so it stays verbatim. For
    // USER-DEFINED + ARRAY types we route the udt_name through quoteIdent.
    let type = col.data_type;
    if (type === "USER-DEFINED") type = quoteIdent(col.udt_name);
    if (type === "ARRAY") type = `${quoteIdent(col.udt_name.replace(/^_/, ""))}[]`;
    if (type === "character varying") type = col.character_maximum_length ? `varchar(${col.character_maximum_length})` : "varchar";
    if (type === "numeric" && col.numeric_precision) type = `numeric(${col.numeric_precision},${col.numeric_scale ?? 0})`;
    let line = `  ${quoteIdent(col.column_name)} ${type}`;
    // column_default is a pg-generated expression (e.g. `nextval('seq'::regclass)`,
    // `now()`, `'literal'::text`) — already self-quoted by pg, stays verbatim.
    if (col.column_default) line += ` DEFAULT ${col.column_default}`;
    if (col.is_nullable === "NO") line += " NOT NULL";
    return line;
  });
  out.push(`CREATE TABLE IF NOT EXISTS public.${quoteIdent(t)} (\n${lines.join(",\n")}\n);`);
}
out.push("");

// 2b. Constraints (PK, unique, FK, check) via pg_get_constraintdef — guarded.
const consRes = await c.query(`
  SELECT con.conname, rel.relname AS table, pg_get_constraintdef(con.oid) AS def
    FROM pg_constraint con
    JOIN pg_class rel ON rel.oid = con.conrelid
    JOIN pg_namespace n ON n.oid = rel.relnamespace
   WHERE n.nspname = 'public' AND rel.relname NOT LIKE 'pg_stat_statements%'
   ORDER BY (con.contype = 'f'), rel.relname, con.conname
`);
for (const r of consRes.rows) {
  // `r.def` is `pg_get_constraintdef()` output — pg-generated, self-quoted,
  // stays verbatim. Names go through the helpers.
  out.push(
    `DO $$ BEGIN\n` +
      `  IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = ${quoteLiteral(r.conname)} AND connamespace = 'public'::regnamespace) THEN\n` +
      `    ALTER TABLE public.${quoteIdent(r.table)} ADD CONSTRAINT ${quoteIdent(r.conname)} ${r.def};\n` +
      `  END IF;\nEND $$;`,
  );
}
out.push("");

// 2c. Non-constraint indexes via pg_indexes.indexdef (skip those backing constraints).
const idxRes = await c.query(`
  SELECT i.indexname, i.indexdef
    FROM pg_indexes i
   WHERE i.schemaname = 'public'
     AND i.tablename NOT LIKE 'pg_stat_statements%'
     AND NOT EXISTS (SELECT 1 FROM pg_constraint con WHERE con.conname = i.indexname)
   ORDER BY i.tablename, i.indexname
`);
for (const r of idxRes.rows) {
  // pg_indexes.indexdef is "CREATE [UNIQUE] INDEX name ON ..." — make idempotent.
  const def = r.indexdef.replace(/^CREATE (UNIQUE )?INDEX /, (_m, u) => `CREATE ${u ?? ""}INDEX IF NOT EXISTS `);
  out.push(`${def};`);
}
out.push("");

await c.end();
mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, out.join("\n") + "\n");
console.log(`wrote ${OUT}: ${tables.length} tables, ${consRes.rows.length} constraints, ${idxRes.rows.length} indexes`);

#!/usr/bin/env node
/**
 * CRM pointer-row drift guard.
 *
 * Cinatra Twenty CRM migration: account / contact / list data lives in
 * Twenty CRM. cinatra keeps lightweight pointer rows in `cinatra.objects`
 * for account + contact only (no list pointer rows — lists dissolve into
 * Twenty Views). Heavy fields (`email`, `title`, `website`, social URLs,
 * `enrichmentStatus`) are read THROUGH the crm-connector facade, NEVER
 * directly off `objects.data` for account/contact rows.
 *
 * This gate forbids:
 *   1. Heavy-field reads from `objects.data` for account/contact rows
 *      (matched as `.data.email`, `.data.title`, ... patterns in source)
 *   2. Any retired `lists_*` MCP primitive call
 *   3. Any retired `accounts_*` / `contacts_*` MCP primitive call
 *
 * Allowlisted surfaces (the only files that may touch the forbidden tokens):
 *   - `extensions/cinatra-ai/crm-connector/**`     (the facade)
 *   - `extensions/cinatra-ai/twenty-connector/**`  (the provider)
 *   - `packages/objects/src/resolvers/crm-resolver.ts` (hydration)
 *   - `scripts/twenty-bootstrap/**`                            (bootstrap + cutover)
 *   - `packages/entity-{accounts,contacts}/**` and `packages/lists/**`
 *     during their deprecation-stub window
 *
 * Out-of-scope by design:
 *   - Generated agent OAS metadata under extensions slash-star slash cinatra
 *     slash star.json plus the .cinatra-published.json marker file (both
 *     regenerated by a separate pipeline; this gate would conflict)
 *   - docs, README.md, scanner self-references
 *
 * Usage:
 *   node scripts/audit/crm-pointer-gate.mjs           # default scan
 *   node scripts/audit/crm-pointer-gate.mjs --json    # JSON output
 *
 * Exit codes:
 *   0  clean
 *   1  one or more violations
 */


/** Path-substring skip. Anything under a path matching one of these is
 *  excluded. Use for generated metadata that lives in extension dirs. */
const SKIP_PATH_SUBSTRINGS = [
  "/cinatra/oas.json",
  "/cinatra/.cinatra-published.json",
  "/__generated__/",
];

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function buildPattern({ token, boundary }) {
  const escaped = token.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  if (boundary === "word") {
    return new RegExp(`(?<![A-Za-z0-9_])${escaped}(?![A-Za-z0-9_])`);
  }
  return new RegExp(escaped);
}

function normalizeRelPath(absPath) {
  return relative(REPO_ROOT, absPath).split(sep).join("/");
}

function isAllowlistedPath(relPath) {
  // The self-contained per-kind gate (`extension-kind-gate.mjs`) is shipped
  // BYTE-IDENTICALLY into every companion agent/workflow repo — it IS the
  // scanner that enumerates the retired-primitive list by design (same as the
  // allowlisted template + OAS gate). Its materialized copies are cloned back
  // to extensions/cinatra-ai/<slug>/. Match
  // by basename so every copy (template + clones) is treated as gate definition,
  // not real usage.
  if (relPath.endsWith("/extension-kind-gate.mjs") || relPath === "extension-kind-gate.mjs") {
    return true;
  }
  return ALLOWLIST_PREFIXES.some((prefix) =>
    prefix.endsWith("/") ? relPath.startsWith(prefix) : relPath === prefix,
  );
}

function shouldSkipFile(relPath) {
  if (SKIP_FILENAMES.has(relPath.split("/").pop() ?? "")) return true;
  if (SKIP_FILE_EXTS.has(extname(relPath))) return true;
  if (SKIP_PATH_SUBSTRINGS.some((s) => relPath.includes(s))) return true;
  return false;
}

export async function* walk(dir) {
  let entries;
  try {
    entries = await readdir(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const entry of entries) {
    const abs = `${dir}/${entry.name}`;
    if (entry.isDirectory()) {
      if (SKIP_DIRS.has(entry.name)) continue;
      yield* walk(abs);
    } else if (entry.isFile()) {
      yield abs;
    }
  }
}

// ---------------------------------------------------------------------------
// Scan
// ---------------------------------------------------------------------------

const PATTERNS = BANNED.map((spec) => ({ ...spec, pattern: buildPattern(spec) }));

export async function runScan() {
  const violations = [];
  for await (const abs of walk(REPO_ROOT)) {
    const relPath = normalizeRelPath(abs);
    if (shouldSkipFile(relPath)) continue;
    if (isAllowlistedPath(relPath)) continue;

    let contents;
    try {
      contents = readFileSync(abs, "utf-8");
    } catch {
      continue;
    }

    const lines = contents.split("\n");
    for (let i = 0; i < lines.length; i++) {
      const line = lines[i] ?? "";
      for (const spec of PATTERNS) {
        if (spec.pattern.test(line)) {
          violations.push({
            path: relPath,
            line: i + 1,
            token: spec.token,
            reason: spec.reason,
            preview: line.trim().slice(0, 160),
          });
        }
      }
      // Heavy-field-pattern check: only flag if the file references account
      // or contact types nearby AND reads `.data.<heavy-field>`.
      if (
        /@cinatra-ai\/entity-(accounts|contacts):/i.test(contents) ||
        /"type":\s*"(account|contact)"/i.test(contents)
      ) {
        for (const heavy of HEAVY_FIELD_PATTERNS) {
          if (heavy.test(line)) {
            violations.push({
              path: relPath,
              line: i + 1,
              token: heavy.source,
              reason:
                "Heavy CRM field read off objects.data — hydrate via @cinatra-ai/crm-connector instead",
              preview: line.trim().slice(0, 160),
            });
            break;
          }
        }
      }
    }
  }
  return violations;
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

const isMain = import.meta.url === `file://${process.argv[1]}`;
if (isMain) {
  const wantJson = process.argv.includes("--json");
  const wantStrict = process.argv.includes("--strict");
  // Fail-closed: scan the whole repo INCLUDING the
  // cloned-back extension tree; without it this gate silently skips extension
  // files. The crm-migration-gate workflow clones the tree before this runs.
  assertExtensionsPresent(REPO_ROOT, "crm-pointer-gate");
  const violations = await runScan();
  if (wantJson) {
    process.stdout.write(JSON.stringify({ ok: violations.length === 0, violations, strict: wantStrict }, null, 2) + "\n");
  } else if (violations.length === 0) {
    process.stdout.write("crm-pointer-gate: clean.\n");
  } else if (wantStrict) {
    process.stderr.write(`crm-pointer-gate: STRICT FAIL — ${violations.length} violation(s):\n`);
    for (const v of violations) {
      process.stderr.write(`  ${v.path}:${v.line}  ${v.token}\n    ${v.reason}\n    ${v.preview}\n`);
    }
  } else {
    // Default mode reports the current debt without failing CI. The migration
    // follow-ups shrink this list; once the count is zero, CI can flip to
    // --strict to lock in the no-regression invariant.
    process.stdout.write(
      `crm-pointer-gate: WARN — ${violations.length} legacy reference(s) to retired surfaces. ` +
        "Re-run with --strict to FAIL on any finding; without --strict the gate is informational only " +
        "while the SKILL.md / taxonomy / inventory-augment migrations land.\n",
    );
  }
  // Default (non-strict) mode always exits 0. --strict exits non-zero when
  // violations are present.
  process.exit(wantStrict && violations.length > 0 ? 1 : 0);
}
